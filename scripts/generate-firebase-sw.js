/**
 * Build script: Generates firebase-messaging-sw.js from environment variables.
 * 
 * Service workers in `public/` cannot access process.env at runtime,
 * so we inject Firebase config at build time.
 * 
 * Usage: node scripts/generate-firebase-sw.js
 * Run automatically before `next build` via the `prebuild` script.
 */

const fs = require('fs')
const path = require('path')

const OUTPUT = path.join(__dirname, '..', 'public', 'firebase-messaging-sw.js')

const required = [
  'NEXT_PUBLIC_FIREBASE_API_KEY',
  'NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN',
  'NEXT_PUBLIC_FIREBASE_PROJECT_ID',
  'NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET',
  'NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID',
  'NEXT_PUBLIC_FIREBASE_APP_ID',
]

// Check for missing env vars
const missing = required.filter((key) => !process.env[key])
if (missing.length > 0) {
  console.error(
    `\n[generate-firebase-sw] Missing required environment variables:\n  ${missing.join('\n  ')}\n`
  )
  console.error('Add them to your .env file. See .env.example for reference.')
  process.exit(1)
}

const config = {
  apiKey: process.env.NEXT_PUBLIC_FIREBASE_API_KEY,
  authDomain: process.env.NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN,
  projectId: process.env.NEXT_PUBLIC_FIREBASE_PROJECT_ID,
  storageBucket: process.env.NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET,
  messagingSenderId: process.env.NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID,
  appId: process.env.NEXT_PUBLIC_FIREBASE_APP_ID,
}

const content = `// Firebase Messaging Service Worker
// AUTO-GENERATED by scripts/generate-firebase-sw.js — DO NOT EDIT MANUALLY
// Source: environment variables at build time

importScripts("https://www.gstatic.com/firebasejs/9.0.0/firebase-app-compat.js")
importScripts("https://www.gstatic.com/firebasejs/9.0.0/firebase-messaging-compat.js")

firebase.initializeApp(${JSON.stringify(config, null, 4)})

const messaging = firebase.messaging()

// Handle background messages
messaging.onBackgroundMessage((payload) => {
    console.log("[SW] Background message received:", payload)

    const title = payload.notification?.title || "New Notification"
    const body = payload.notification?.body || ""
    const link = payload.data?.link || "/dashboard"

    const options = {
        body,
        icon: "/icon-192x192.png",
        badge: "/icon-72x72.png",
        tag: "notification-" + Date.now(),
        data: {
            link,
            ...payload.data,
        },
        actions: [
            { action: "open", title: "Open" },
            { action: "dismiss", title: "Dismiss" },
        ],
    }

    self.registration.showNotification(title, options)
})

// Handle notification click
self.addEventListener("notificationclick", (event) => {
    console.log("[SW] Notification clicked:", event)

    event.notification.close()

    if (event.action === "dismiss") {
        return
    }

    const link = event.notification.data?.link || "/dashboard"

    event.waitUntil(
        clients.matchAll({ type: "window", includeUncontrolled: true }).then((clientList) => {
            // Focus existing window if open
            for (const client of clientList) {
                if (client.url.includes(self.location.origin) && "focus" in client) {
                    client.navigate(link)
                    return client.focus()
                }
            }
            // Open new window
            if (clients.openWindow) {
                return clients.openWindow(link)
            }
        })
    )
})
`

fs.writeFileSync(OUTPUT, content, 'utf-8')
console.log(`[generate-firebase-sw] Generated ${path.relative(process.cwd(), OUTPUT)}`)
